
fun main() {

    var i = 1 // Variable de control
    do{ // Inicia el bloque de se va a ejecutar mínimo una vez
        print("\n\t Hola $i")
        i++ // Incremento
    }while(i<=4) // Condición

}
/*
*  Usamos do-while
*  cuando queremos ejecutar un bloque
*  al menos una vez y que dependiendo
*  de una condición este repita o no...
*
* */